#les variables representent des conteneur qui permettent de stoker des données utilisable par le programme 
#chaque variable est liée à un type de donnée (integer,string,boolean,float)

#"integer" ou encore "int" représente un nombre entier . Exemple: 20; 15 ;48  
age = 20  #integer 
print(age)

#"string" ou encore "str" représente une chaine de caractères . Exemple: "bonjour"; "bonjour jelien comment vas-tu?" ;"56" 
#En python tout ce qui est écrit dans les guillemet est une chaine de caracter. Exemple: "56"; "école"
nom="hacker"
print(nom)

date="2026-7-6"
print(date)

#"Boolean" ou encore "boll" ne peut representé que deux valeur soit VRAI soit FAUX
est_admin=True
print(est_admin)

est_etudiant=False
print(est_etudiant)

#"float" servent à représenté des nombres decimaux . Exemple: 15.26; 225.62 ;100.00 
prix=12.23
print(prix)

moyenne=15.45
print(moyenne)

